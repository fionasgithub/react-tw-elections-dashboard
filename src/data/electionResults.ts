// ── Helper functions ──

export function getCountyResults(): CountyResult[] {
  return countyResults;
}

export function getCountyResultById(
  countyId: string,
): CountyResult | undefined {
  return countyResults.find((r) => r.countyId === countyId);
}

export function getTownshipsByCounty(countyId: string): TownshipResult[] {
  return townshipResults.filter((t) => t.countyId === countyId);
}

/**
 * Determine the winning party for a county based on highest vote rate.
 * Returns undefined if tied or no data.
 */
export function getWinnerParty(
  candidates: { party: string; voteRate: number }[],
): string | undefined {
  if (!candidates.length) return undefined;
  const sorted = [...candidates].sort((a, b) => b.voteRate - a.voteRate);
  if (sorted.length > 1 && sorted[0].voteRate === sorted[1].voteRate)
    return undefined;
  return sorted[0].party;
}
